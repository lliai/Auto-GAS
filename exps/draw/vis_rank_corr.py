def plot_rank_corr(gt_list, zc_list, x_label, y_label, save_path):
    # transform log to zc
    # zc_list = [math.log(x) for x in zc_list]

    fig, ax = plt.subplots(1, 1, figsize=FIGSIZE)
    plt.scatter(gt_list, zc_list, cmap='rainbow')
    # plt.colorbar()
    plt.grid(lw=0.5, ls='-.')
    plt.text(
        min(gt_list) + 0.01,
        max(zc_list) * 0.998,
        f'Spearman={spearman(gt_list, zc_list):.2f}',
        style='italic')
    plt.xlabel(x_label, fontsize=GLOBAL_LABELSIZE - 10)
    plt.ylabel(y_label, fontsize=GLOBAL_LABELSIZE - 10)
    formatter = ticker.ScalarFormatter(useMathText=True)
    formatter.set_scientific(True)
    formatter.set_powerlimits((0, 0))
    ax.yaxis.set_major_formatter(formatter)
    plt.savefig(save_path, dpi=GLOBAL_DPI, bbox_inches='tight')


def plot_rank_corr_v2(gt_list, zc_list, x_label, y_label, save_path):
    fig, ax = plt.subplots(1, 1, figsize=FIGSIZE)

    # Plot the scatter points
    plt.scatter(gt_list, zc_list, cmap='rainbow')

    # Calculate and plot the best fit line
    slope, intercept = np.polyfit(gt_list, zc_list, 1)
    plt.plot(gt_list, [slope * x + intercept for x in gt_list], 'r-')

    # Display the grid and the Spearman correlation value
    plt.grid(lw=0.5, ls='-.')
    spearman_corr = spearman(gt_list, zc_list)
    plt.text(
        0.4,
        max(zc_list) * 0.998,
        f'Spearman={spearman_corr:.2f}',
        style='italic')

    # Setting labels, formatter and saving the plot
    plt.xlabel(x_label, fontsize=GLOBAL_LABELSIZE - 10)
    plt.ylabel(y_label, fontsize=GLOBAL_LABELSIZE - 10)
    formatter = ticker.ScalarFormatter(useMathText=True)
    formatter.set_scientific(True)
    formatter.set_powerlimits((0, 0))
    ax.yaxis.set_major_formatter(formatter)
    plt.savefig(save_path, dpi=GLOBAL_DPI, bbox_inches='tight')
    plt.show()


